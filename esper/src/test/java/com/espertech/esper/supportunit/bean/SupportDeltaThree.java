package com.espertech.esper.supportunit.bean;

public class SupportDeltaThree {
    private final String k0;
    private final String p0;
    private final String p4;

    public SupportDeltaThree(String k0, String p0, String p4) {
        this.k0 = k0;
        this.p0 = p0;
        this.p4 = p4;
    }

    public String getK0() {
        return k0;
    }

    public String getP0() {
        return p0;
    }

    public String getP4() {
        return p4;
    }
}
